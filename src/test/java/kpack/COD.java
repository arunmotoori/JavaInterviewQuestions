package kpack;

public class COD {
	
	public COD() {
		
		System.out.println("Inside constructor");
		
	}
	
	public COD(int a) {
		
		System.out.println("Inside constructor "+a);
		
	}
	
	public COD(double a) {
		
		System.out.println("Inside constructor "+a);
		
	}
	
	public COD(int a,double b) {
		
		System.out.println("Inside constructor "+(a+b));
		
	}
	
	public COD(double a,int b) {
		
		System.out.println("Inside constructor "+(a+b));
		
	}
	

}
